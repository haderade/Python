seconds_per_hour = 60 * 60
print("一時間あたりの秒数：" + str(seconds_per_hour))

seconds_per_day = seconds_per_hour * 24
print("一日あたりの秒数：" + str(seconds_per_day))

print("一日あたりの秒数 / 一時間あたりの秒数：" + str(seconds_per_day/seconds_per_hour))

print("一日あたりの秒数 // 一時間あたりの秒数：" + str(seconds_per_day//seconds_per_hour))
